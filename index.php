			<h2>"Particle Interconnect" (PI) overview</h2>
			<h3>Description</h3>
			<p>Particle Interconnect <font style="color: green;"><b>replaces soldering, wire bonding, and on-bump bonding</b></font> with <font style="color: green;"><b>environmentally safe, diffusion bonding at very low contact force, and no wiping.</b></font></p>
			<p><font style="color: green;"><b>Bonding temperature can vary from -270° C to > 450° C, allowing to minimize thermal stress due to CTE mismatch.</b></font> Mono-metallic bonds (Particle Interconnect) unlike bi-metallic ones (solder) <font style="color: green;"><b>do not decay over time, due to temperature.</b></font></p>
			<p>Particle Interconnect is the ultimate technology in <font style="color: green;"><b>electrical / mechanical / thermal interconnection</b></font> with <font style="color: green;"><b>best price and best performance</b></font> (not just price/performance) compared to other <a href="Competition.html">interconnect technologies</a>.</p>
			<p>Particle Interconnect is the only interconnect method with lead length of <font style="color: green;"><b>0.001" (25 μ)</b></font>, bandwidth<font style="color: green;"><b> > 100 GHz</b></font>, and a lifetime of<font style="color: green;"><b> > 1.5 million insertions, with extreme acceleration tolerance.</b></font></p>
			<p>This unique technology creates an oxide penetrating, non-wiping gas tight connection that allows <font style="color: green;"><b>electrical connection in presence of adhesive, oil, and dirt.</b></font></p>
			<p>Eliminating wirebonding, PCB drilling, and reducing PCB blind vias leads to <font style="color: green;"><b>device miniaturization by more than an order of magnitude</b></font>, with reduced power needs and costs, through the use of <a href="MCM.html">MCM</a> or <a href="SIP.html">SIP</a>.</p>
			<p>Because of reduced process steps and reduced material costs, the interconnect cost <font style="color: green;"><b>half the current industry practices.</b></font> Additionally, mounting these connection points is faster, easier, and requires little specialized equipment or skilled labor.</p>
			<h3>Usage</h3>
			<p>All steps of electronic product development: prototyping, <a href="TestSockets.html">test</a> (controlled impedance), <a href="BurnInSockets.html">burn-in</a>, <a href="EmulationSockets.html">emulation</a>/<a href="CharacterizationSockets.html">characterization</a>, <a href="FieldReplacableSocketExamples.html">field replacable</a>, and <a href="ProductionSockets.html">production</a>. Particle Interconnect technology reduce costs of burn-in die before assembly (<a href="KGDSockets.html">KGD</a>).</p>
			<p>Particle Interconnect provides rugged miniature interconnects for integrated circuit die, packaged devices, low-cost <a href="Probe.html">probe cards,</a> and <a href="SurfaceMountPackageTestFixture.html">test fixtures.</a></p>
			<p>Since bond strength is a function of contact force (it determines contact area), one can:</p>
			<ul>
				<li><font style="color: green;"><b>Test the final product without testing individual parts</b></font>. Since defective parts can be easily replaced.</li>
				<li><font style="color: green;"><b>Repair and upgrade the system in the field</b></font>.</li>
				<li><font style="color: green;"><b>Rapidly disassemble and reuse or recycle electronic components</b></font>.</li>
			</ul>
			<p><font style="color: green;"><b>Anti-stiction coating for MEMS</b></font>, since particles do not wear out.</p>
