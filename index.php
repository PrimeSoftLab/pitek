<h2>Applications</h2>
<h3>Contactor sockets</h3>
<p>Particle Interconnect is used as interconnect between surface mount devices, load boards, testers, programmers, and other repeat-insertion applications. With a total interconnect between device under test (DUT) lead and test circuit trace of less than 0.001".</p>
<p>The typical resistance of a conventional test socket begins at approximately 30 milli-Ohms. As a conventional test socket begins to wear out, the contact resistance begins to increase and contact resistances of over several hundred milli-Ohms are common. By the time a conventional test socket receives 10,000 insertions, it can begin to fail in exacting test applications.</p>
<p>The Particle Interconnect begins with 3 milli-Ohms of contact resistance, and is still 3 milli-Ohms after 1,000,000 insertions on a rigid board and 100,000 insertions on a flex board.</p>
<h3>Ultra high frequency sockets</h3>
<p>A common problem encountered in high-frequency test fixtures is the impedance mismatch between the device under test and the test circuitry. This mismatch is typically caused by the use of a conventional test socket or contactor assembly. A conventional test socket has built-in resistance. Due to its physical characteristics, it forces the test circuitry to be placed a minimum distance from the device under test. All of these factors contribute to increased resistance (R), capacitance (C), and inductance (L).</p>
<p>However, due to the superior physical characteristics of Particle Interconnect (PI), and because Particle Interconnect contacts exhibit the same electrical characteristics as a solder connection, use of Particle Interconnect can effectively eliminate the impedance mismatch of conventional contactor assemblies.</p>
<h3>Printed circuit boards</h3>
<p>PCBs can be made from rigid or flex specialty dielectric substrates, or etched BeCu.</p>
