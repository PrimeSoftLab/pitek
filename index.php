			<h3>Electrical performance</h3>
			<p>The Particle Interconnect joint formed by pressing particles through the metal oxides into the lead has electrical stability and performances superior to a solder joint. Contact resistance is extremely low, measuring 3 - 5 milli-Ohms, and does not change even after 1.5 Million remates.</p>
			<p>Particle Interconnect technology received an endorsement from Hewlett-Packard, Microwave Division for the excellent high frequency (> 20 GHz) performance they obtained when testing high frequency IC parts.</p>
			<p>The contaminates and oxides present on metal contact surfaces mandate the wiping action to make reliable electrical connection. The wiping action high compressive forces cause plastic deformation of the contact surface thus exposing the clean metal surface beneath.</p>
			<p>Work performed by different research groups indicates that the electrical connections are made through the asperities present on the electrical surface. Equations were developed by one research consortium which indicate there is a relationship between contact-resistance, contact-force, the number of asperities, the size of the asperity, and the asperities distribution.</p>
			<p>Particle Interconnect technology forms a contacting surface that has a large number of asperities. These asperities have very sharp and hard points due to the nature of the particle or crystal used (e.g., diamond). The sharp points do not dull or wear since the crystal is much harder than the opposing metal surface.</p>
			<p>A key feature of Particle Interconnect technology is the use of many parallel connections. The Particle Interconnect surface is like conductive sandpaper and like sandpaper has many points to penetrate any opposing surface. The Particle Interconnect surface is also similar to a micro-bed-of-nails. Like the bed-of-nails, the Particle Interconnect surface has many parallel paths for contact reliability and durability; a long cycle life; high current capacity; very low contact resistance; and forms a gas-tight joint for better performance in harsh dynamic environments.</p>
			<p>The Particle Interconnect joint is a pressure only connection which requires no sliding or wiping action. Thus the connection is not an element in the electrical chain, but is a matched impedance connection. With no impedance mismatch, there is no reflected energy. Reducing the amplitude of the reflected signal allows for faster signal transmission with greater signal fidelity.</p>
			<h3>Low contact force requirement</h3>
			<p>Sharp particles in Particle Interconnect concentrate contact force into a very small area. The high pressure pierces oxides and other contaminants on surfaces without requiring large amounts of force on the contact. Reliable, gas-tight connections can be made with Particle Interconnect with as little as 10 grams of force per contact. This low level of force is enough to drive the particles into the mating surface and provide a contact resistance of 3 to 5 milli-Ohms.</p>
			<h3>Re-mateability</h3>
			<p>Insertions of the particles do very little damage to the mating surface. This provides very long re-mate life with very little degradation of the connection. Since there is no wiping action, contact coatings stay intact. Connection tests against standard solder balls have proven a life of over 30,000 insertions with no significant damage to the contact's performance.</p>
			<h3>Material compliance</h3>
			<p>These particles can be applied to many different substrates - flexible, rigid, metallic, and non-metallic.</p>
			<p>This ability coupled with the very low contact force gives us the capability to make very reliable interposers with large compliances out of materials that would collapse if exposed to the normally required contact forces. Compliances from 0.002" to 0.020" result.</p>
