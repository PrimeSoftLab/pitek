				<ul>
					<li>Low-cost heat exchanger designed for modular dissipation needs.</li>
					<li>Removable die locator (precisor) aligns all die on 0.010" edge-to-edge mosaic tile pattern of silicon.</li>
					<li>Precisor is accurately aligned by corner guide pins in heat sink.</li>
					<li>Removable die PI interposer sockets die to PCB and spring loads die against heat sink.</li>
					<li>Interposer is accurately aligned by corner guide pins in heat sink.</li>
					<li>Removable die PI interposers with 0.001" - 0.008" travel on 0.001" - 0.030" pitch are available.</li>
					<li>Removable multilayer PCB interconnects all die together with controlled impedance from die pad to die to pad.</li>
					<li>PI vias between laminate layers allow 0.002" "blind" vias by punching through adhesive layer during lamination.</li>
					<li>PCB is accurately aligned by corner guide pins in heat sink.</li>
					<li>Removable package PI interposer sockets package to PCB and spring loads package to PCB.</li>
					<li>MCM is accurately aligned and held to PCB by corner guide pins for manual or automatic insertion.</li>
					<li>Package interposers with 0.08"-0.020" travel on 0.010"-0.050" pitch with either peripheral or array patterns available.</li>
					<li>Die are removable by simply unstacking layers.</li>
					<li>Both bumped and wire bond die pads are mixable.</li>
				</ul>
